-- RCA cleanup script
-- :keep_latest - how many latest MR jobs to keep in RCA

DELETE FROM workflow
WHERE workflowid NOT IN (
  SELECT workflowid
  FROM workflow
  ORDER BY lastupdatetime
  DESC
  LIMIT :keep_latest
);
